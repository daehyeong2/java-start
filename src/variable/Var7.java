package variable;

public class Var7 {

  public static void main(String[] args) {
    int a = 100; // 정수 (100과 같은 개발자가 직접 입력한 값들을 리터럴이라 부름)
    double b = 10.5; // 실수
    boolean c = true; // 불리
    char d = 'a'; // 문자
    String e = "abc"; // 문자열

    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }
}
